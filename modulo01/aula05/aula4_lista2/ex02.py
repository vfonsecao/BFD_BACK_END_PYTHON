var_a = float(input("Digite o primeiro número (A): "))
var_b = float(input("Digite o segundo número (B): "))

soma = var_a + var_b
subtracao = var_a - var_b
multiplicacao = var_a * var_b
divisao = var_a / var_b  # divisão normal

print(f"A soma entre {var_a} e {var_b} é: {soma}")
print(f"A subtração entre {var_a} e {var_b} é: {subtracao}")
print(f"A multiplicação entre {var_a} e {var_b} é: {multiplicacao}")
print(f"A divisão de {var_a} por {var_b} é: {divisao}")